class Tournament:
    def __init__(self):
        self.start = ""
        self.end = ""
        self.name = ""
        self.city = ""
        self.link = ""
        self.flag = ""

    def setStart(self, start):
        self.start = start

    def setEnd(self, end):
        self.end = end

    def setName(self, name):
        self.name = name

    def setCity(self, city):
        self.city = city

    def setLink(self, link):
        self.link = link

    def setFlag(self, flag):
        self.flag = flag